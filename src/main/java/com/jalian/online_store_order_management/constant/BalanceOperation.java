package com.jalian.online_store_order_management.constant;

/**
 * The BalanceOperation enum represents the type of balance operations that can be performed.
 * <p>
 * It defines two operations:
 * <ul>
 *   <li>{@code PLUS}: Represents an addition operation to increase a balance.</li>
 *   <li>{@code MINUS}: Represents a subtraction operation to decrease a balance.</li>
 * </ul>
 * </p>
 *
 * @author amirhosein jalian
 */
public enum BalanceOperation {

    PLUS, MINUS;
}
